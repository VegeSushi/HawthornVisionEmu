LcdSimulator::LcdSimulator() : is_open(false), cursor_x(0), cursor_y(0), kbd_head(0), kbd_tail(0) {
    clear();
}

LcdSimulator::~LcdSimulator() { close(); }

bool LcdSimulator::init() {
#if defined(__linux__) || defined(_WIN32)
    if (SDL_Init(SDL_INIT_VIDEO) < 0) return false;
    int w = 20 * (5 * PIXEL_SCALE + BLOCK_PADDING_X) + 20;
    int h = 4 * (7 * PIXEL_SCALE + BLOCK_PADDING_Y) + 20;
    window = SDL_CreateWindow("Arduino Computer Emulator", SDL_WINDOWPOS_CENTERED, SDL_WINDOWPOS_CENTERED, w, h, SDL_WINDOW_SHOWN);
    if (!window) return false;
    renderer = SDL_CreateRenderer(window, -1, SDL_RENDERER_ACCELERATED);
    if (!renderer) return false;
    SDL_StartTextInput();
#endif
    is_open = true;
    return true;
}

void LcdSimulator::clear() {
    memset(grid, ' ', sizeof(grid));
    cursor_x = 0; cursor_y = 0;
}

void LcdSimulator::setCursor(uint8_t x, uint8_t y) {
    if (x < 20) cursor_x = x;
    if (y < 4)  cursor_y = y;
}

void LcdSimulator::write(char c) {
    // Standardize to uppercase for the retro LCD display output
    if (c >= 'a' && c <= 'z') c -= 32;

    if (c == '\n' || c == '\r') {
        cursor_x = 0;
        if (cursor_y < 3) {
            cursor_y++;
        } else {
            for (int r = 0; r < 3; ++r) memcpy(grid[r], grid[r + 1], 20);
            memset(grid[3], ' ', 20);
            cursor_y = 3;
        }
        return;
    }

    if (cursor_x < 20 && cursor_y < 4) {
        grid[cursor_y][cursor_x] = c;
        cursor_x++;
        if (cursor_x >= 20) {
            cursor_x = 0;
            if (cursor_y < 3) {
                cursor_y++;
            } else {
                for (int r = 0; r < 3; ++r) memcpy(grid[r], grid[r + 1], 20);
                memset(grid[3], ' ', 20);
                cursor_y = 3;
            }
        }
    }
}

void LcdSimulator::print(const char* str) {
    while (*str) write(*str++);
}

void LcdSimulator::pushKey(char c) {
    // CRITICAL FIX: Only allow clean, printable ASCII or structural terminal returns 
    if ((c >= 32 && c <= 126) || c == '\n' || c == '\b') {
        if (c >= 'a' && c <= 'z') c -= 32; // Natively convert lowercase to uppercase
        uint8_t next = (kbd_head + 1) % sizeof(kbd_buffer);
        if (next != kbd_tail) {
            kbd_buffer[kbd_head] = c;
            kbd_head = next;
        }
    }
}

bool LcdSimulator::available() { return kbd_head != kbd_tail; }

char LcdSimulator::read() {
    if (kbd_head == kbd_tail) return 0;
    char c = kbd_buffer[kbd_tail];
    kbd_tail = (kbd_tail + 1) % sizeof(kbd_buffer);
    return c;
}

void LcdSimulator::update(unsigned int delayMs) {
#if defined(__linux__) || defined(_WIN32)
    if (!is_open) return;
    SDL_Event e;
    while (SDL_PollEvent(&e)) {
        if (e.type == SDL_QUIT) { close(); return; }
        
        else if (e.type == SDL_TEXTINPUT) {
            for (size_t i = 0; e.text.text[i] != '\0'; ++i) {
                pushKey(e.text.text[i]);
            }
        }
        else if (e.type == SDL_KEYDOWN) {
            SDL_Keycode key = e.key.keysym.sym;
            if (key == SDLK_BACKSPACE) pushKey('\b');
            else if (key == SDLK_RETURN || key == SDLK_KP_ENTER) pushKey('\n');
        }
    }

    SDL_SetRenderDrawColor(renderer, 155, 180, 15, 255);
    SDL_RenderClear(renderer);
    renderMatrix();
    SDL_RenderPresent(renderer);
    if (delayMs > 0) SDL_Delay(delayMs);
#endif
}

void LcdSimulator::close() {
#if defined(__linux__) || defined(_WIN32)
    SDL_StopTextInput();
    if (renderer) SDL_DestroyRenderer(renderer);
    if (window) SDL_DestroyWindow(window);
    if (is_open) SDL_Quit();
#endif
    is_open = false;
}

