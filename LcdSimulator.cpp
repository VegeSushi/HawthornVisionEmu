void LcdSimulator::renderMatrix() {
    int cellW = 5 * PIXEL_SCALE + BLOCK_PADDING_X;
    int cellH = 7 * PIXEL_SCALE + BLOCK_PADDING_Y;
    for (int y = 0; y < 4; ++y) {
        for (int x = 0; x < 20; ++x) {
            int drawX = 15 + (x * cellW);
            int drawY = 15 + (y * cellH);
            SDL_SetRenderDrawColor(renderer, 140, 165, 10, 40);
            SDL_Rect bgRect = { drawX - 2, drawY - 2, 5 * PIXEL_SCALE + 4, 7 * PIXEL_SCALE + 4 };
            SDL_RenderFillRect(renderer, &bgRect);
            
            drawCharacter(drawX, drawY, grid[y][x]);
            
            if (x == cursor_x && y == cursor_y && (SDL_GetTicks() / 400) % 2 == 0) {
                SDL_SetRenderDrawColor(renderer, 20, 30, 5, 200);
                SDL_Rect curRect = { drawX, drawY + 6 * PIXEL_SCALE, 5 * PIXEL_SCALE, PIXEL_SCALE };
                SDL_RenderFillRect(renderer, &curRect);
            }
        }
    }
}

void LcdSimulator::drawCharacter(int startX, int startY, char c) {
    uint8_t fontIdx = 0;
    if (c >= 32 && c <= 126) {
        fontIdx = c - 32;
    } else {
        fontIdx = 0; 
    }

    SDL_SetRenderDrawColor(renderer, 20, 30, 5, 240);
    for (int row = 0; row < 7; ++row) {
        uint8_t rowData = FONT_BITMAP[fontIdx][row];
        for (int col = 0; col < 5; ++col) {
            if (rowData & (0x10 >> col)) {
                SDL_Rect pixelRect = { startX + (col * PIXEL_SCALE), startY + (row * PIXEL_SCALE), PIXEL_SCALE - 1, PIXEL_SCALE - 1 };
                SDL_RenderFillRect(renderer, &pixelRect);
            }
        }
    }
}
